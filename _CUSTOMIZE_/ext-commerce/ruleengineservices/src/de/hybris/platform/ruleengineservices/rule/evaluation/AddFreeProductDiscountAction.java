package de.hybris.platform.ruleengineservices.rule.evaluation;

import de.hybris.platform.ruleengineservices.rao.DiscountRAO;
import de.hybris.platform.ruleengineservices.rao.EntriesSelectionStrategyRPD;
import de.hybris.platform.ruleengineservices.rao.RuleEngineResultRAO;

import java.util.List;


/**
 * AddFreeProductDiscountAction class that adds order entry level discounts such that a given quantity of products are
 * free
 *
 */
public interface AddFreeProductDiscountAction
{
	/**
	 * Adds order level discounts to order entries contained within selection strategies. Strategies provide data used
	 * for selection of entries that will be discounted.
	 *
	 * @param entriesSelectionStrategyRPD
	 *           Selection strategy holds information about which order entries should be discounted or consumed
	 * @param quantity
	 *           Number of free items that are discounted
	 * @param consumed
	 *           Number of entries that are consumed for each free item
	 * @param result
	 *           DiscountRAOs generated by the action and added to order
	 * @param ruleContext
	 *           an optional context object that can be used to provide additional information for the returned
	 *           DiscountRAOs
	 */
	List<DiscountRAO> addDiscount(EntriesSelectionStrategyRPD entriesSelectionStrategyRPD, int quantity, int consumed,
			RuleEngineResultRAO result, Object ruleContext);

}
